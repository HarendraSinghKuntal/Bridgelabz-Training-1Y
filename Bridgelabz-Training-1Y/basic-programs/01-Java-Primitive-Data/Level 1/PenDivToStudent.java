class PenDivToStudent {
    public static void main(String[] args) {
        int a = 14;
        int b = 3;
        int PenEachGets = a / b;
        int RemainingPens = a % b;

        System.out.println("The pen each student gets is: " + PenEachGets +
                " and the remaining pens are: " + RemainingPens);
    }
}
